<h2> Reglas </h2>
<ol>
	<li>
		Jugar no cuesta. Es una quiniela entre amigos. Me oiste, SEGOB?
	</li>
	<li>
		Puedes modificar tus resultados en cualquier momento, hasta un minuto antes de que empiece el partido. El sistema cerrará el registro en cuanto dé la hora marcada. Verifica los horarios. 
		<br>
		Todos los horarios mostrados son en hora de la Ciudad de México (GMT-6)
	</li>
	<li>
		Cada partido será puntuado de la siguiente manera:
		<br>
		1 punto por atinar al ganador (o al empate), 3 puntos por atinar al marcador, 0 en caso contrario
	</li>

	<li>
		El marcador por default en caso de que no alcances a guardar tus datos, será de 0-0
	</li>
	<li>
		Para los partidos despues de la ronda de grupos, el  resultado que cuenta es el que se dé tras terminados los dos tiempos reglamentarios.
		Es decir, no contarán los goles en penalties o tiemposs extras.

	</li>
	<li>
		Cabe la posibilidad de que existan premios. Cuando esté confirmado, se hará saber por este medio. 
		En caso de que los haya, el ganador deberá seguir a @efectotequila.
	</li>
	<li>
		Cualquier situación no prevista, será resuelta por los administradores.
	</li>
</ol>
